score = 75
scoreOfA = 90
scoreOfB = 80
scoreOfC = 70
scoreOfD = 60

if score >= scoreOfA:
  print('Your grade is A.')
else:
  if score >= scoreOfB:
    print('Your grade is B.')

  else:
    if score >= scoreOfC:
      print('Your grade is C.')
    else:
      if score >= scoreOfD:
        print('Your grade is D.')
      else:
        print('Your grade is F.')
